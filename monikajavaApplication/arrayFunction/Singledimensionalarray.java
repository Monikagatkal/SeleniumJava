package arrayFunction;

public class Singledimensionalarray {

	public static void main(String[] args) {
		
		int numbers[] = {1,2,3,4,5};
		
		System.out.println("------------------Integer Single array-------------------------------");
		System.out.println("1st number="+ numbers[0]);
		System.out.println("2nd number=" +numbers[1]);
		System.out.println("3rd number=" +numbers[2]);
		System.out.println("4th number=" +numbers[3]);
		System.out.println("5th number=" +numbers[4]);
		
		
		System.out.println("------------------Float Single array-------------------------------");
		float num[] = {1.2f,2.3f,3.4f,4.5f,5.6f};
		System.out.println("Float value ="+ num[0]);
		System.out.println("Float value ="+ num[1]);
		System.out.println("Float value ="+ num[2]);
		System.out.println("Float value ="+ num[3]);
		System.out.println("Float value ="+ num[4]);
		
		
		System.out.println("----------------------Character single array---------------------------");
		char ch[]= {'a','b','c','d'};
		System.out.println("character is ="+ ch[0]);
		System.out.println("character is ="+ ch[1]);
		System.out.println("character is ="+ ch[2]);
		System.out.println("character is ="+ ch[3]);
		
		
		System.out.println("-------------String Array-----------------------------------");
		String arr[] = {"monika","mony","moni","mona"};
		System.out.println("string name is ="+ arr[0] +"," + arr[1] +"," + arr[2]+ "," + arr[3]);
		
		System.out.println("-------------Boolean Array-----------------------------------");
		
		boolean arr1[] = {true,false,true,true,false};
		System.out.println("Boolean is ="+ arr1[0]);
		System.out.println("Boolean is ="+ arr1[1]);
		System.out.println("Boolean is ="+ arr1[2]);
		System.out.println("Boolean is ="+ arr1[3]);
		System.out.println("Boolean is ="+ arr1[4]);
		
	}

}
